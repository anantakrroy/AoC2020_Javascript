const pathArr = input.split("\n");
const pathLen = pathArr.length;

function treeNum(r, d) {
    let idx = 0;
    let treeCount = 0;
    for (let i = d; i < pathLen; i += d) {
        idx = (idx + 31 * i * d + r) % 31;
        if (pathArr[i][idx] === "#") {
            treeCount++;
        }
    }
    return treeCount;
}

